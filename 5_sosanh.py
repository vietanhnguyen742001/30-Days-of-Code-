# not equal 
print(20==20)
print("anh" == "em")
print(20 != 10)
print("anh" != "em")

# > , >=, < ,<=
print(10>=10)
print(15<10)
print(15<=15)

# logical operators 
num = 15
print( " Number is in the range [10, 20]:", num >= 10 and num <= 20 )
print( " Number is in the range [10, 20]:", 10 <= num <= 20 )

num_1 = 30 
print( " Number is out of the range [10, 20]:", num_1 < 10 or num_1 > 20 )

bol = False 
print(not bol)
print(not 6 > 5)
